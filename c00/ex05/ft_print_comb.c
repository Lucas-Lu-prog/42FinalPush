#include <unistd.h>

void	ft_putchar(char a)
{
	write(1, &a, 1);
}

void	print_things(char a, char b, char c)
{
	ft_putchar(a);
	ft_putchar(b);
	ft_putchar(c);
	if (a != '7')
	{
		ft_putchar(',');
		ft_putchar(' ');
	}
}

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;

	a = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				print_things(a, b, c);
				c ++;
			}
			b ++;
		}
		a ++;
	}
}
